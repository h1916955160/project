package user;

public class garbage {
    private int garbage_id;
    private String garbage_name;
    private String garbage_content;
    private String garbage_type;

    public garbage()
    {
        super();
    }
    public int getGarbage_id() {
        return garbage_id;
    }

    public void setGarbage_id(int garbage_id) {
        this.garbage_id = garbage_id;
    }

    public String getGarbage_name() {
        return garbage_name;
    }

    public void setGarbage_name(String garbage_name) {
        this.garbage_name = garbage_name;
    }

    public String getGarbage_content() {
        return garbage_content;
    }

    public void setGarbage_content(String garbage_content) {
        this.garbage_content = garbage_content;
    }

    public String getGarbage_type() {
        return garbage_type;
    }

    public void setGarbage_type(String garbage_type) {
        this.garbage_type = garbage_type;
    }
}
